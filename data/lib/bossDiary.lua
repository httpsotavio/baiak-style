function bossInArea(area, boss)
  for x = area[1].x - 1, area[2].x + 1 do
    for y = area[1].y - 1, area[2].y + 1 do
      local pos = {x=x, y=y, z=area[1].z}
      local m = getTopCreature(pos).uid
      if boss then
        if m ~= 0 and isMonster(m) and getCreatureName(m) == boss then
          return true
        end
      else
        if m ~= 0 and isMonster(m) then
          return true
        end
      end
    end 
  end
  return false
end

function getPlayersInRoom(from, to)
    local players = {}
    for _, pid in pairs(getPlayersOnline()) do
        if isInRange(getPlayerPosition(pid), from, to) then 
            players[#players+1] = pid
        end
    end
    return players
end